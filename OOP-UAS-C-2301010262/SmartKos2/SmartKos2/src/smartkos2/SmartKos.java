    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            String sql = "INSERT INTO tb_kos (nama_kos, pemilik, tanggal_masuk, fasilitas, status) VALUES (?, ?, ?, ?, ?)";
            Connection conn = koneksi.configDB();
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, formNama.getText());
            pst.setString(2, formPemilik.getText());

            // Format tanggal dari JDateChooser
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String tanggal = sdf.format(formTanggal.getDate());
            pst.setString(3, tanggal);

            pst.setString(4, formFasilitas.getSelectedItem().toString());
            pst.setString(5, formStatus.getSelectedItem().toString());

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan.");
            tampilData();
            resetForm();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal simpan: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try {
            int row = tabel.getSelectedRow();
            if (row == -1) {
                JOptionPane.showMessageDialog(this, "Pilih data yang ingin diedit.");
                return;
            }

            String namaLama = tabel.getValueAt(row, 1).toString(); // kolom ke-1 = nama_kos

            String sql = "UPDATE tb_kos SET nama_kos=?, pemilik=?, tanggal_masuk=?, fasilitas=?, status=? WHERE nama_kos=?";
            Connection conn = koneksi.configDB();
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, formNama.getText());
            pst.setString(2, formPemilik.getText());

            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String tanggal = sdf.format(formTanggal.getDate());
            pst.setString(3, tanggal);

            pst.setString(4, formFasilitas.getSelectedItem().toString());
            pst.setString(5, formStatus.getSelectedItem().toString());
            pst.setString(6, namaLama);

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil diubah.");
            tampilData();
            resetForm();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal edit data: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        try {
            int row = tabel.getSelectedRow();
            if (row == -1) {
                JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus.");
                return;
            }

            String nama = tabel.getValueAt(row, 1).toString();

            int konfirmasi = JOptionPane.showConfirmDialog(this, "Yakin hapus data?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (konfirmasi == JOptionPane.YES_OPTION) {
                String sql = "DELETE FROM tb_kos WHERE nama_kos=?";
                Connection conn = koneksi.configDB();
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, nama);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
                tampilData();
                resetForm();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal hapus data: " + e.getMessage());
        }
    }//GEN-LAST:event_btnHapusActionPerformed

