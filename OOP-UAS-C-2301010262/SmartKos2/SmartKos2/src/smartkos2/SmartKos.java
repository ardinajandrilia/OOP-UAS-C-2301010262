    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        int row = tabel.getSelectedRow();
        if (row != -1) {
            String nama = tabel.getValueAt(row, 1).toString();
            String pemilik = tabel.getValueAt(row, 2).toString();
            String tanggalStr = tabel.getValueAt(row, 3).toString();
            String fasilitas = tabel.getValueAt(row, 4).toString();
            String status = tabel.getValueAt(row, 5).toString();

            formNama.setText(nama);
            formPemilik.setText(pemilik);
            formFasilitas.setSelectedItem(fasilitas);
            formStatus.setSelectedItem(status);

            try {
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                java.util.Date tanggal = sdf.parse(tanggalStr);
                formTanggal.setDate(tanggal);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal parsing tanggal: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_tabelMouseClicked
    private void tampilData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("Nama Kos");
        model.addColumn("Pemilik");
        model.addColumn("Tanggal Masuk");
        model.addColumn("Fasilitas");
        model.addColumn("Status");

        try {
            int no = 1;
            String sql = "SELECT * FROM tb_kos";
            Connection conn = koneksi.configDB();
            Statement stm = conn.createStatement();
            ResultSet res = stm.executeQuery(sql);

            while (res.next()) {
                model.addRow(new Object[]{
                    no++,
                    res.getString("nama_kos"),
                    res.getString("pemilik"),
                    res.getDate("tanggal_masuk"),
                    res.getString("fasilitas"),
                    res.getString("status")
                });
            }
            tabel.setModel(model);
            tampilData();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal tampilkan data: " + e.getMessage());
        }
    }

    private void resetForm() {
        formNama.setText("");
        formPemilik.setText("");
        formTanggal.setDate(null);
        formFasilitas.setSelectedIndex(0);
        formStatus.setSelectedIndex(0);
    }

